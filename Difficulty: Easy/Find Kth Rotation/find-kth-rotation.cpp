class Solution {
  public:
    int findKRotation(vector<int> &nums) {
        // Code Here
        int low=0,high=nums.size()-1;
        while(low<high)
        {
            int mid=(low+high)/2;
            if(nums[mid]>nums[high]) low=mid+1;
            else high=mid;
        }
        return low;
    }
};


