class Solution
{
    public:
       Node* reverse(Node* head) {
        Node* prev = nullptr;
        Node* current = head;
        Node* next = nullptr;
        while (current != nullptr) {
            next = current->next;
            current->next = prev;
            prev = current;
            current = next;
        }
        return prev;
    }

    Node* addOne(Node *head) 
    {
        // Your Code here
        // return head of list after adding one
        head = reverse(head);
        
        Node* current = head;
        Node* prev = nullptr;
        int carry = 1;
        
        while (current != nullptr) {
            int sum = current->data + carry;
            carry = sum / 10;
            current->data = sum % 10;
            
            prev = current;
            current = current->next;
        }
        
        if (carry > 0) {
            prev->next = new Node(carry);
        }
        
        // Reverse the linked list back to original order
        return reverse(head);
            
    }
};

